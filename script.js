let currentQuestion = 0;
let scores = { A: 0, B: 0, C: 0, D: 0, E: 0, F: 0, G: 0, H: 0 };

function startTest() {
    document.getElementById('start-screen').classList.remove('active');
    document.getElementById('question-screen').classList.add('active');
    showQuestion();
}

function showQuestion() {
    const q = questions[currentQuestion];
    document.getElementById('q-number').textContent = `Q${currentQuestion + 1} / 8`;
    document.getElementById('q-text').textContent = q.text;
    document.getElementById('progress').style.width = `${((currentQuestion) / 8) * 100}%`;

    const optionsDiv = document.getElementById('options');
    optionsDiv.innerHTML = '';
    
    q.options.forEach((opt, idx) => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.textContent = opt.text;
        btn.onclick = () => selectOption(idx);
        optionsDiv.appendChild(btn);
    });
}

function selectOption(idx) {
    const q = questions[currentQuestion];
    const selected = q.options[idx];
    
    for (let key in selected.scores) {
        scores[key] += selected.scores[key];
    }

    currentQuestion++;

    if (currentQuestion < questions.length) {
        document.getElementById('question-screen').classList.remove('active');
        setTimeout(() => {
            document.getElementById('question-screen').classList.add('active');
            showQuestion();
        }, 100);
    } else {
        showResult();
    }
}

function showResult() {
    document.getElementById('question-screen').classList.remove('active');
    document.getElementById('result-screen').classList.add('active');

    let maxScore = -999;
    let resultType = 'A';
    
    for (let key in scores) {
        if (scores[key] > maxScore) {
            maxScore = scores[key];
            resultType = key;
        }
    }

    const result = results[resultType];
    document.getElementById('result-emoji').textContent = result.emoji;
    document.getElementById('result-type').textContent = result.type;
    document.getElementById('result-tagline').textContent = result.tagline;
    document.getElementById('result-desc').textContent = result.desc;
    document.getElementById('result-tip').textContent = result.tip;
}

function restartTest() {
    currentQuestion = 0;
    scores = { A: 0, B: 0, C: 0, D: 0, E: 0, F: 0, G: 0, H: 0 };
    
    document.getElementById('result-screen').classList.remove('active');
    document.getElementById('start-screen').classList.add('active');
}

function showToast(message) {
    const toast = document.getElementById('toast');
    toast.textContent = message;
    toast.classList.add('show');
    setTimeout(() => toast.classList.remove('show'), 2000);
}

